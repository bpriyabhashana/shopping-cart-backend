package com.store.shoppingcart.Dto;

public class CartPriceMap {
    private double penguinTotalPrice;
    private double horseTotalPrice;
    private double totalCartPrice;

    public double getPenguinTotalPrice() {
        return penguinTotalPrice;
    }

    public void setPenguinTotalPrice(double penguinTotalPrice) {
        this.penguinTotalPrice = penguinTotalPrice;
    }

    public double getHorseTotalPrice() {
        return horseTotalPrice;
    }

    public void setHorseTotalPrice(double horseTotalPrice) {
        this.horseTotalPrice = horseTotalPrice;
    }

    public double getTotalCartPrice() {
        return totalCartPrice;
    }

    public void setTotalCartPrice(double totalCartPrice) {
        this.totalCartPrice = totalCartPrice;
    }
}
