package com.store.shoppingcart.Dto;

public class PriceListMap {
    private int amount;
    private double pricePenguin;
    private double priceHorse;


    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public double getPricePenguin() {
        return pricePenguin;
    }

    public void setPricePenguin(double pricePenguin) {
        this.pricePenguin = pricePenguin;
    }

    public double getPriceHorse() {
        return priceHorse;
    }

    public void setPriceHorse(double priceHorse) {
        this.priceHorse = priceHorse;
    }
}
